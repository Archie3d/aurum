#include "aurum/dsp/Functions.h"

namespace au {
namespace dsp {

double poly_blep(double t, double dt)
{
    // 0 <= t < 1
    if (t < dt) {
        t /= dt;
        return t+t - t*t - 1.0;
    }
    // -1 < t < 0
    else if (t > 1.0 - dt) {
        t = (t - 1.0) / dt;
        return t*t + t+t + 1.0;
    }
    // 0 otherwise
    else return 0.0;
}


} // namespace dsp
} // namespace au
